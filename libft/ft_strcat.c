#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*origin_dest;

	origin_dest = dest;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (origin_dest);
}
